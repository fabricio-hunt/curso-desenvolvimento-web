// Estrutura de controle if...else em JavaScript

// Exemplo 1: if...else simples
let hora = 10;

if (hora < 12) {
    console.log("Bom dia!");
} else {
    console.log("Boa tarde!");
}

// Exemplo 2: if...else if...else com múltiplas condições
let nota = 85;

if (nota >= 90) {
    console.log("Excelente!");
} else if (nota >= 70) {
    console.log("Bom!");
} else {
    console.log("Precisa melhorar.");
}
